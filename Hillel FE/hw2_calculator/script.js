const a = Number(prompt('Enter the first number:'));
const b = Number(prompt('Enter the second number:'));

const sum = a + b;
const diff = a - b;
const mult = a * b;
const div = a / b;

console.log('Calculations are finished!');
console.log('Sum: ' + a + ' + ' + b + ' = ' + sum);
console.log('Diff: ' + a + ' - ' + b + ' = ' + diff);
console.log('Mult: ' + a + ' * ' + b + ' = ' + mult);
console.log('Div: ' + a + ' / ' + b + ' = ' + div);